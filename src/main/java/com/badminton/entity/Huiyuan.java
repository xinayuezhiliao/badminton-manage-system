package com.badminton.entity;

public class Huiyuan {
    private Integer id;
    private String bianhao;
    private String name;
    private String phone;
    private String type;
    private String zhuangtai;
    private String addtime;

    public Integer getId() { return id; }
    public void setId(Integer id) { this.id = id; }
    public String getBianhao() { return bianhao; }
    public void setBianhao(String bianhao) { this.bianhao = bianhao; }
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getPhone() { return phone; }
    public void setPhone(String phone) { this.phone = phone; }
    public String getType() { return type; }
    public void setType(String type) { this.type = type; }
    public String getZhuangtai() { return zhuangtai; }
    public void setZhuangtai(String zhuangtai) { this.zhuangtai = zhuangtai; }
    public String getAddtime() { return addtime; }
    public void setAddtime(String addtime) { this.addtime = addtime; }
}